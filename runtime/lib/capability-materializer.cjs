'use strict';

const capabilityCatalog = require('./capability-catalog.cjs');

function createCapabilityMaterializerHelpers(deps) {
  const {
    fs,
    path,
    runtime,
    workflowRegistry,
    getProjectExtDir
  } = deps;

  function ensureProjectWorkflowLayout() {
    const projectExtDir = getProjectExtDir();
    const workflowLayout = workflowRegistry.syncProjectWorkflowLayout(projectExtDir, { write: true });
    const workflowPaths = workflowRegistry.getProjectWorkflowPaths(projectExtDir);
    const registry = runtime.readJson(workflowPaths.registryPath);

    return {
      projectExtDir,
      workflowLayout,
      workflowPaths,
      registry
    };
  }

  function saveRegistry(workflowPaths, registry) {
    runtime.writeJson(workflowPaths.registryPath, registry);
  }

  function upsertNamedEntry(entries, nextEntry) {
    const list = Array.isArray(entries) ? entries.slice() : [];
    const index = list.findIndex(item => item && item.name === nextEntry.name);

    if (index >= 0) {
      list[index] = {
        ...list[index],
        ...nextEntry
      };
      return list;
    }

    list.push(nextEntry);
    return list;
  }

  function writeGeneratedFile(filePath, content, force, created, reused) {
    if (fs.existsSync(filePath) && !force) {
      reused.push(filePath);
      return;
    }

    runtime.ensureDir(path.dirname(filePath));
    fs.writeFileSync(filePath, content, 'utf8');
    created.push(filePath);
  }

  function buildCapabilitySpecContent(definition, plan) {
    const materialization = definition.materialization || {};

    return [
      `# ${materialization.spec_title || `${definition.title} Capability`}`,
      '',
      `> Generated by emb-agent capability materializer for \`${definition.name}\`.`,
      '',
      '## Purpose',
      '',
      definition.description || '',
      '',
      '## Primary Entry',
      '',
      `- Capability: \`${definition.name}\``,
      `- Preferred runtime entry: \`capability run ${definition.name}\``,
      '',
      '## Project Adaptation',
      '',
      '- Replace these defaults with project-specific truth sources, constraints, and closure criteria.',
      '- Keep this file capability-scoped. Do not mix unrelated workflow rules here.',
      '- Prefer generated workflow templates for host-facing execution docs; keep this spec as the durable routing contract.',
      '',
      '## Focus Areas',
      '',
      ...(
        Array.isArray(materialization.focus_areas) && materialization.focus_areas.length > 0
          ? materialization.focus_areas.map(item => `- ${item}`)
          : ['- FILL: project-specific focus areas for this capability']
      ),
      '',
      '## Review Axes',
      '',
      ...(
        Array.isArray(materialization.review_axes) && materialization.review_axes.length > 0
          ? materialization.review_axes.map(item => `- ${item}`)
          : ['- FILL: review axes if this capability needs extra review pressure']
      ),
      '',
      '## Notes / Templates',
      '',
      `- Template: \`${plan.template_name}\` -> \`${plan.default_output}\``,
      `- Workflow spec path: \`${plan.spec_relative_path}\``,
      ''
    ].join('\n');
  }

  function buildCapabilityTemplateContent(definition, plan) {
    const materialization = definition.materialization || {};

    return [
      `# {{PROJECT_NAME}} ${definition.title} Workflow`,
      '',
      `- Capability: ${definition.name}`,
      `- Generated Spec: ${plan.spec_name}`,
      `- Default Output: ${plan.default_output}`,
      '',
      '## Goal',
      '',
      materialization.summary || definition.description || '',
      '',
      '## Entry',
      '',
      `- Preferred runtime entry: \`capability run ${definition.name}\``,
      '',
      '## Inputs',
      '',
      '- FILL: required truth sources',
      '- FILL: required task / project context',
      '- FILL: blockers or gates',
      '',
      '## Outputs',
      '',
      '- FILL: durable outputs',
      '- FILL: verification or closure evidence',
      '',
      '## Guardrails',
      '',
      '- Keep the workflow scoped to this capability.',
      '- Prefer explicit truth and evidence over conversational memory.',
      '- Update the paired capability spec when project rules change materially.',
      ''
    ].join('\n');
  }

  function buildMaterializationPlan(definition) {
    const materialization = definition && definition.materialization && typeof definition.materialization === 'object'
      ? definition.materialization
      : null;

    if (!materialization) {
      return null;
    }

    return {
      spec_name: materialization.spec_name || `capability-${definition.name}`,
      spec_relative_path: `specs/${materialization.spec_name || `capability-${definition.name}`}.md`,
      template_name: materialization.template_name || `${definition.name}-workflow`,
      template_relative_path: `templates/${materialization.template_name || `${definition.name}-workflow`}.md.tpl`,
      default_output: materialization.default_output || `docs/workflows/${definition.name}.md`
    };
  }

  function materializeCapability(name, options = {}) {
    const force = options.force === true;
    const definition = capabilityCatalog.requireCapabilityDefinition(name);

    if (!definition.materializable || !definition.materialization) {
      throw new Error(`Capability cannot be materialized: ${definition.name}`);
    }

    const plan = buildMaterializationPlan(definition);
    const {
      projectExtDir,
      workflowLayout,
      workflowPaths,
      registry
    } = ensureProjectWorkflowLayout();
    const created = [];
    const reused = [];
    const specAbsolutePath = path.join(projectExtDir, plan.spec_relative_path);
    const templateAbsolutePath = path.join(projectExtDir, plan.template_relative_path);

    writeGeneratedFile(
      specAbsolutePath,
      buildCapabilitySpecContent(definition, plan),
      force,
      created,
      reused
    );
    writeGeneratedFile(
      templateAbsolutePath,
      buildCapabilityTemplateContent(definition, plan),
      force,
      created,
      reused
    );

    registry.specs = upsertNamedEntry(registry.specs, {
      name: plan.spec_name,
      title: definition.materialization.spec_title || `${definition.title} Capability`,
      path: plan.spec_relative_path,
      summary: definition.materialization.summary || `Project-local capability spec for ${definition.name}.`,
      auto_inject: true,
      selectable: true,
      priority: 70,
      apply_when: {
        specs: [plan.spec_name]
      },
      focus_areas: definition.materialization.focus_areas || [],
      extra_review_axes: definition.materialization.review_axes || [],
      preferred_notes: definition.materialization.preferred_notes || [plan.default_output],
      default_agents: definition.materialization.default_agents || []
    });
    registry.templates = upsertNamedEntry(registry.templates, {
      name: plan.template_name,
      source: plan.template_relative_path,
      description: definition.materialization.summary || `Project-local workflow template for ${definition.name}.`,
      default_output: plan.default_output
    });
    saveRegistry(workflowPaths, registry);

    return {
      capability: definition.name,
      workflow_layout: workflowLayout,
      plan,
      created: created.map(filePath => path.relative(projectExtDir, filePath).replace(/\\/g, '/')),
      reused: reused.map(filePath => path.relative(projectExtDir, filePath).replace(/\\/g, '/')),
      registry_path: path.relative(projectExtDir, workflowPaths.registryPath).replace(/\\/g, '/')
    };
  }

  function listMaterializableCapabilities() {
    return capabilityCatalog
      .listCapabilityDefinitions()
      .filter(definition => definition.materializable && definition.materialization)
      .map(definition => ({
        name: definition.name,
        title: definition.title,
        description: definition.description,
        plan: buildMaterializationPlan(definition)
      }));
  }

  function materializeCapabilitySet(target, options = {}) {
    const normalizedTarget = capabilityCatalog.normalizeCapabilityName(target || 'all');
    const definitions =
      !normalizedTarget || normalizedTarget === 'all'
        ? capabilityCatalog
            .listCapabilityDefinitions()
            .filter(definition => definition.materializable && definition.materialization)
        : [capabilityCatalog.requireCapabilityDefinition(normalizedTarget)];

    const results = definitions.map(definition => materializeCapability(definition.name, options));

    return {
      target: normalizedTarget || 'all',
      results,
      created: results.flatMap(item => item.created || []),
      reused: results.flatMap(item => item.reused || [])
    };
  }

  return {
    buildMaterializationPlan,
    listMaterializableCapabilities,
    materializeCapability,
    materializeCapabilitySet
  };
}

module.exports = {
  createCapabilityMaterializerHelpers
};
